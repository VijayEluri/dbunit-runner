SET SCHEMA dev;

DROP TABLE IF EXISTS dept;
CREATE TABLE dept (
    deptno INTEGER PRIMARY KEY,
    dname VARCHAR(14) ,
    loc VARCHAR(13)
);
 
DROP TABLE IF EXISTS emp;
CREATE TABLE emp (
    empno INTEGER PRIMARY KEY,
    ename VARCHAR(10),
    job VARCHAR(9),
    mgr INTEGER,
    hiredate DATE,
    sal DECIMAL(7,2),
    comm DECIMAL(7,2),
    deptno INTEGER
);
